//  while loops are used to repeat a block of code as same like for loop but in that condition 
// where we don't know where the condition should be stoped.   
// You don’t know how many times the loop will run and The loop depends on a condition that
// may change dynamically 
// in this loop initializatin is declared out of the loop like let a = 5;

// *************************syntax**************************
